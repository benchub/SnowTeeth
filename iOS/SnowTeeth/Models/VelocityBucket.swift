import Foundation

enum VelocityBucket: String, CaseIterable {
    case downhillHard = "DOWNHILL_HARD"
    case downhillMedium = "DOWNHILL_MEDIUM"
    case downhillEasy = "DOWNHILL_EASY"
    case idle = "IDLE"
    case uphillEasy = "UPHILL_EASY"
    case uphillMedium = "UPHILL_MEDIUM"
    case uphillHard = "UPHILL_HARD"
}
